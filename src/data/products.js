// Helper functions
export const getProductsByCategory = (category) => {
  return PRODUCTS.filter(product => product.category === category);
};

export const getProductById = (id) => {
  return PRODUCTS.find(product => product.id === parseInt(id));
};

// Helper function to get product price based on quantity
export const getProductPrice = (productId, quantity = '1kg') => {
  const product = getProductById(productId);
  if (!product) return 0;
  
  const price = product.prices[quantity];
  return price || product.prices['1kg']; // fallback to 1kg price if quantity not found
};

export const getAllCategories = () => {
  return Object.values(CATEGORIES);
};

export const getCategoryProductCount = (category) => {
  return getProductsByCategory(category).length;
};

